// Indian meal templates for different regional cuisines

/// Meal template for a single meal
class MealTemplate {
  final String id;
  final String name;
  final String nameLocal;
  final String region;
  final double caloriesPer100g;
  final double proteinPer100g;
  final double carbsPer100g;
  final double fatPer100g;
  final double fiberPer100g;
  final double? vitaminDPer100g;
  final double? vitaminB12Per100g;
  final double? ironPer100g;
  final double? calciumPer100g;
  final String mealType;
  final String doshaBenefit; // which dosha benefits most

  const MealTemplate({
    required this.id,
    required this.name,
    required this.nameLocal,
    required this.region,
    required this.caloriesPer100g,
    required this.proteinPer100g,
    required this.carbsPer100g,
    required this.fatPer100g,
    required this.fiberPer100g,
    this.vitaminDPer100g,
    this.vitaminB12Per100g,
    this.ironPer100g,
    this.calciumPer100g,
    required this.mealType,
    required this.doshaBenefit,
  });

  /// Scale nutrients based on portion size
  Map<String, double> getScaledNutrients(double portionGrams) {
    final scale = portionGrams / 100;
    return {
      'calories': caloriesPer100g * scale,
      'proteinG': proteinPer100g * scale,
      'carbsG': carbsPer100g * scale,
      'fatG': fatPer100g * scale,
      'fiberG': fiberPer100g * scale,
      'vitaminDMcg': (vitaminDPer100g ?? 0) * scale,
      'vitaminB12Mcg': (vitaminB12Per100g ?? 0) * scale,
      'ironMg': (ironPer100g ?? 0) * scale,
      'calciumMg': (calciumPer100g ?? 0) * scale,
    };
  }
}

/// Indian regional meal templates
class IndianMealTemplates {
  /// Get all templates for a region
  static List<MealTemplate> getTemplatesForRegion(String region) {
    switch (region.toLowerCase()) {
      case 'north_indian':
        return northIndianTemplates;
      case 'south_indian':
        return southIndianTemplates;
      case 'bengali':
        return bengaliTemplates;
      case 'gujarati':
        return gujaratiTemplates;
      case 'mixed':
      default:
        return [
          ...northIndianTemplates,
          ...southIndianTemplates,
          ...bengaliTemplates,
          ...gujaratiTemplates,
        ];
    }
  }

  /// Get templates for a specific meal type and region
  static List<MealTemplate> getTemplatesForMealType(
    String mealType,
    String region,
  ) {
    return getTemplatesForRegion(
      region,
    ).where((t) => t.mealType == mealType).toList();
  }

