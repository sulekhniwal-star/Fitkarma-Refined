  /// Get a full day's meal plan for a region and dosha
  static Map<String, MealTemplate> getFullDayPlan(String region, String dosha) {
    final templates = getTemplatesForRegion(region);

    final breakfast = templates.firstWhere(
      (t) =>
          t.mealType == 'breakfast' &&
          (t.doshaBenefit == dosha || t.doshaBenefit == 'vata'),
      orElse: () => templates.firstWhere((t) => t.mealType == 'breakfast'),
    );

    final lunch = templates.firstWhere(
      (t) =>
          t.mealType == 'lunch' &&
          (t.doshaBenefit == dosha || t.doshaBenefit == 'pitta'),
      orElse: () => templates.firstWhere((t) => t.mealType == 'lunch'),
    );

    final dinner = templates.firstWhere(
      (t) =>
          t.mealType == 'dinner' &&
          (t.doshaBenefit == dosha || t.doshaBenefit == 'vata'),
      orElse: () => templates.firstWhere((t) => t.mealType == 'dinner'),
    );

    final snack = templates.firstWhere(
      (t) =>
          t.mealType == 'snack' &&
          (t.doshaBenefit == dosha || t.doshaBenefit == 'kapha'),
      orElse: () => templates.firstWhere((t) => t.mealType == 'snack'),
    );

    return {
      'breakfast': breakfast,
      'lunch': lunch,
      'dinner': dinner,
      'snack': snack,
    };
  }
